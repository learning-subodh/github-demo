package javaBasics;

public class TwoDimensionArray {

	public static void main(String[] args) {
		//2-d array are used when we want to keep value attached with some variable or column.
		//it stores the data in table form.
		//key value pair storing.
		
		int i[][] = new int[3][5];
		i[0][0] = 3;
		
		
		
		System.out.println(i.length);
		System.out.println(i[0].length);

	}

}
